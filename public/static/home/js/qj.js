function quanjiasel(si,qu,area,shop_num,shop_name){
      var select_si = document.getElementById(si);
      var select_qu = document.getElementById(qu);
      var select_area = document.getElementById(area);
      var shop_num = document.getElementById(shop_num);
      var shop_name = document.getElementById(shop_name);
      var option_si = "<option value=''>--選擇城市--</option>";
      $.each(quanjia,function(i,index){
            option_si += "<option value='"+i+"'>"+i+"</option>";
      });
      select_si.innerHTML = option_si;
      select_si.onchange = function(){
            if($(this).val() !== ""){
                var option_qu = "<option value=''>--選擇地區--</option>";
                var option_area = "<option value=''>--選擇地區--</option>";
                $.each(quanjia[$(this).val()],function(i,index){
                      option_qu += "<option value='"+i+"'>"+i+"</option>";
                });
                select_qu.innerHTML = option_qu;
                select_area.innerHTML = option_area;
                shop_num.innerHTML = "";
                shop_name.innerHTML = "";
            }else{
                select_qu.innerHTML = "<option value=''>--選擇地區--</option>";
                select_area.innerHTML = "<option value=''>--選擇地區--</option>";
                shop_num.innerHTML = "";
                shop_name.innerHTML = "";
            }
      }
      select_qu.onchange = function(){
            if($(this).val() !== ""){
                var option_area = "<option value=''>--選擇地區--</option>";
                $.each(quanjia[$(select_si).find('option:selected').html()][$(this).val()],function(i,index){
                      var str_index = index.split("--");
                      option_area += "<option shop-num='"+str_index[1]+"' shop-name='"+str_index[2]+"' value='"+str_index[0]+"'>"+str_index[0]+"</option>";
                });
                select_area.innerHTML = option_area;
                shop_num.innerHTML = "";
                shop_name.innerHTML = "";
            }else{
                select_area.innerHTML = "<option value=''>--選擇地區--</option>";
                shop_num.innerHTML = "";
                shop_name.innerHTML = "";
            }
      }
      select_area.onchange = function(){
            if($(this).val() !== ""){
                shop_num.innerHTML = "店號："+$(this).find('option:selected').attr('shop-num');
                shop_name.innerHTML = "店名："+$(this).find('option:selected').attr('shop-name');
                document.getElementById("input_hide_num").value = $(this).find('option:selected').attr('shop-num');
                document.getElementById("input_hide_name").value = $(this).find('option:selected').attr('shop-name');
            }else{
                shop_num.innerHTML = "";
                shop_name.innerHTML = "";
            }
      }
}